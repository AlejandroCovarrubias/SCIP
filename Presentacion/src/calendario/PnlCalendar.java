    private void btn_mesAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mesAnteriorActionPerformed
        if (fechaEstablecer.getMes() > 1) {
            fechaEstablecer.setMes(fechaEstablecer.getMes() - 1);
        } else {
            if (fechaEstablecer.getAnho() == 2020 & fechaEstablecer.getMes() == 1) {
                JOptionPane.showMessageDialog(
                        this,
                        "El calendario abarca un periodo comprendido entre: "
                                + "\n\r01/01/2020 a 31/12/2050 "
                                + "\n\rPor lo tanto no se puede visualizar el mes ni año anterior.",
                        "Periodo Desfasado",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                fechaEstablecer.setMes(12);
                fechaEstablecer.setAnho(fechaEstablecer.getAnho() - 1);
            }
        }
        establecerCalendario();
    }//GEN-LAST:event_btn_mesAnteriorActionPerformed

    private void btn_mesSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mesSiguienteActionPerformed
        if (fechaEstablecer.getMes() < 12) {
            fechaEstablecer.setMes(fechaEstablecer.getMes() + 1);
        } else {
            if (fechaEstablecer.getAnho() == 2050 & fechaEstablecer.getMes() == 12) {
                JOptionPane.showMessageDialog(
                        this,
                        "El calendario abarca un periodo comprendido entre: "
                                + "\n\r01/01/2020 a 31/12/2050"
                                + "\n\rPor lo tanto no se puede visualizar el mes ni año siguiente.",
                        "Periodo Desfasado",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                fechaEstablecer.setMes(1);
                fechaEstablecer.setAnho(fechaEstablecer.getAnho() + 1);
            }
        }
        establecerCalendario();
    }//GEN-LAST:event_btn_mesSiguienteActionPerformed

